package view;

public class GUIPlayerCards {

    private int black;
    private int blue;
    private int green;
    private int orange;
    private int pink;
    private int rainbow;
    private int red;
    private int white;
    private int yellow;

    GUIPlayerCards() {
        this.black = 0;
        this.blue = 0;
        this.green = 0;
        this.orange = 0;
        this.pink = 0;
        this.rainbow = 0;
        this.red = 0;
        this.white = 0;
        this.yellow = 0;
    }

    public int getBlack() {
        return black;
    }

    public void setBlack(int black) {
        this.black = black;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getOrange() {
        return orange;
    }

    public void setOrange(int orange) {
        this.orange = orange;
    }

    public int getPink() {
        return pink;
    }

    public void setPink(int pink) {
        this.pink = pink;
    }

    public int getRainbow() {
        return rainbow;
    }

    public void setRainbow(int rainbow) {
        this.rainbow = rainbow;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getWhite() {
        return white;
    }

    public void setWhite(int white) {
        this.white = white;
    }

    public int getYellow() {
        return yellow;
    }

    public void setYellow(int yellow) {
        this.yellow = yellow;
    }
}
